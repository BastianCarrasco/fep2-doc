<script setup>
import { ref, onMounted } from 'vue';

const capituloData = ref(null);
const errorMessage = ref('');

onMounted(async () => {
    try {
        const response = await import('../textosbases/bt-capitulo-15-innovaciones.json'); // Asegúrate que la ruta sea correcta
        capituloData.value = response.default;
    } catch (error) {
        console.error('Error al cargar el Capítulo 15 (Bases Técnicas):', error);
        errorMessage.value = 'No se pudo cargar la información del Capítulo 15 (Bases Técnicas).';
    }
});
</script>

<template>
    <div class="capitulo-container">
        <h1 class="main-title" v-if="capituloData">{{ capituloData.titulo_capitulo }}</h1>

        <div v-if="capituloData && capituloData.innovaciones" class="content">
            <div v-for="innovacion in capituloData.innovaciones" :key="innovacion.numero" class="section-block">
                <h2 class="section-title">{{ innovacion.numero }} {{ innovacion.titulo }}</h2>
                <p v-if="innovacion.descripcion">{{ innovacion.descripcion }}</p>
            </div>
        </div>

        <p v-else-if="errorMessage" class="error-message">{{ errorMessage }}</p>
        <p v-else class="loading-message">Cargando "Capítulo 15: Innovaciones (Bases Técnicas)"...</p>
    </div>
</template>

